//constructor of the obstacle class that uses the grid and block class to create an obstacle
Obstacle::Obstacle(Vector2 position)
{
    this -> position = position;

    for(unsigned int row = 0; row < grid.size(); row++)
    {
        for (unsigned int col = 0; col < grid[0].size(); col++)
        {
            if(grid[row][col] == 1)
            {
                float posx = position.x + col * 3;
                float posy = position.y + row * 3;
                Block block = Block({posx, posy});
                blocks.push_back(block);
            }
        }
    }
}

//method to draw the obstacle using the block method
void Obstacle::Draw()
{
    for(auto& block: blocks)
    {
        block.Draw();
    }
}